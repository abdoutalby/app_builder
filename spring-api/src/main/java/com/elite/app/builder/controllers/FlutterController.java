    @GetMapping(value = "/download/{fileName}", produces = MediaType.APPLICATION_OCTET_STREAM_VALUE)
    public ResponseEntity<?> downloadFileAsZip(@PathVariable(value = "fileName") String fileName) {
        try {
            // Compress the file to a zip format in-memory
            ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
            zipFile(fileName, byteArrayOutputStream);

            // Create InputStreamResource for the zip
            byte[] zipBytes = byteArrayOutputStream.toByteArray();
            InputStreamResource resource = new InputStreamResource(new ByteArrayInputStream(zipBytes));

            return ResponseEntity.ok()
                    .contentType(MediaType.APPLICATION_OCTET_STREAM)
                    .header(HttpHeaders.CONTENT_DISPOSITION, "attachment; filename=\"" + fileName + ".zip\"")
                    .contentLength(zipBytes.length)
                    .body(resource);

        } catch (IOException e) {
            return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)
                    .body("Error creating zip file: " + e.getMessage());
        }
    }
    public void zipFile(String fileName, ByteArrayOutputStream outputStream) throws IOException {
        // Define the source file path
        Path filePath = Paths.get("app_name/build/app/outputs/flutter-apk/", fileName);

        if (!Files.exists(filePath)) {
            throw new FileNotFoundException("File not found: " + fileName);
        }

        try (ZipOutputStream zipOutputStream = new ZipOutputStream(outputStream);
             InputStream inputStream = Files.newInputStream(filePath)) {

            // Add a zip entry (file inside the zip)
            ZipEntry zipEntry = new ZipEntry(filePath.getFileName().toString());
            zipOutputStream.putNextEntry(zipEntry);

            // Write the file content to the zip output stream
            byte[] buffer = new byte[4096];
            int bytesRead;
            while ((bytesRead = inputStream.read(buffer)) != -1) {
                zipOutputStream.write(buffer, 0, bytesRead);
            }

            zipOutputStream.closeEntry();
        }
    }

